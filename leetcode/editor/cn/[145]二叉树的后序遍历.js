var postorderTraversal = function(root) {
    let result = [];
    blNode(root,result);
    return result;
};

function blNode(root,result) {
    if(root){
        let left = root.left;
        let right = root.right;
        blNode(left,result);
        blNode(right,result);
        result.push(root.val);
    }
}
